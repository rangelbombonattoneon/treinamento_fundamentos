# comandos executados pelo IRB:

# Tipo de dados: INT
numero = 25
numero.class # o class é utilizado para saber qual o tipo de dados da váriavel.

# Tipo de dados: Float
valor = 12.50
valor.class # float é quando possui um número decimal.

# Tipo de dados: Boolean
boo = true
boo.class # Boolean define se é verdadeiro ou falso.

# Tipo de dados: String
mensagem = "Olá Mundo"
mensagem.class # String define qualquer caracter alfanumérico, onde o valor deve estar dentro das "".

# Tipo de dados: Array
avengers_array = ['capitão américa','homem de ferro','thor']
avengers_array.class # Para mostrar qual o tipo de dados referente a essa variavel.
avengers_array[0] # todo array começa da posição 0 e esse comando serve para mostrar o conteúdo conforme a posição informada.

# Tipo de dados: Symbol
texto_symbol = :ruby_symbol
texto_symbol.class
texto_symbol.object_id # descobre onde é a posição na memória, as variaveis desse tipo sempre ocupam a mesma posição

# Tipo de dados: Hash
listcourse_hash = {course: 'ruby', language: 'pr-Br', locale: 'onebitcode.com'}
listcourse_hash[:course] # forma que é chamado a informação de hash


